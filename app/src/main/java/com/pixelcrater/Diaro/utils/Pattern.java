package com.pixelcrater.Diaro.utils;

/**
 * Class for storing folder patterns
 */
public class Pattern {

    public final String patternTitle;
    public final int patternRepeat;
    public final int patternThumb;


    public Pattern(String patternTitle, int patternRepeat, int patternThumb) {
        this.patternTitle = patternTitle;
        this.patternRepeat = patternRepeat;
        this.patternThumb = patternThumb;
    }
}
